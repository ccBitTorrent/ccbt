def create_po_file(
    lang: str,
    lang_name: str,
    entries: list[dict[str, str]],
    output_path: Path,
    get_translation_func,
) -> None:
    """Create complete .po file with translations."""
    now = datetime.now().strftime("%Y-%m-%d %H:%M%z")

    # Plural forms for each language
    plural_forms = {
        "hi": "nplurals=2; plural=(n != 1);",
        "ur": "nplurals=2; plural=(n != 1);",
        "fa": "nplurals=2; plural=(n == 0 || n == 1);",
        "arc": "nplurals=2; plural=(n != 1);",
    }

    header = f"""msgid ""
msgstr ""
"Project-Id-Version: ccBitTorrent 0.1.0\\n"
"Report-Msgid-Bugs-To: \\n"
"POT-Creation-Date: 2024-01-01 00:00+0000\\n"
"PO-Revision-Date: {now}\\n"
"Last-Translator: ccBitTorrent Team\\n"
"Language-Team: {lang_name}\\n"
"Language: {lang}\\n"
"MIME-Version: 1.0\\n"
"Content-Type: text/plain; charset=UTF-8\\n"
"Content-Transfer-Encoding: 8bit\\n"
"Plural-Forms: {plural_forms.get(lang, "nplurals=2; plural=(n != 1);")}\\n"

"""

    output_lines = [header]

    for entry in entries:
        msgid = entry["msgid"]
        if not msgid:  # Skip header
            continue

        translation = get_translation_func(msgid)

        # Format the entry
        output_lines.append(f'msgid "{escape_po(msgid)}"')
        output_lines.append(f'msgstr "{escape_po(translation)}"')
        output_lines.append("")

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(output_lines))



def escape_po(s: str) -> str:
    """Escape string for .po file format."""
    if not s:
        return ""
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")


def parse_po_template(template_path: Path) -> list[dict[str, str]]:
    """Parse .po template file and return list of entries."""
    with open(template_path, encoding="utf-8") as f:
        content = f.read()

    entries = []
    lines = content.split("\n")
    i = 0

    while i < len(lines):
        line = lines[i].strip()
        if line.startswith("msgid "):
            # Extract msgid
            msgid = line[6:].strip('"')
            i += 1
            # Handle multi-line msgid
            while i < len(lines) and lines[i].strip().startswith('"'):
                msgid += lines[i].strip().strip('"')
                i += 1

            # Find msgstr (may be empty in template)
            msgstr = ""
            if i < len(lines) and lines[i].strip().startswith("msgstr "):
                msgstr = lines[i][7:].strip('"')
                i += 1
                while i < len(lines) and lines[i].strip().startswith('"'):
                    msgstr += lines[i].strip().strip('"')
                    i += 1

            if msgid:  # Skip empty header entry
                entries.append({"msgid": msgid, "msgstr": msgstr})
        else:
            i += 1

    return entries



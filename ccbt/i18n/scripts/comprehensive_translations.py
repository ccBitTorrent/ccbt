def get_translation(msgid: str, lang: str) -> str:
    """Get translation for a message ID."""
    if lang == "zh":
        return ZH_TRANSLATIONS.get(msgid, msgid)
    if lang == "ja":
        return JA_TRANSLATIONS.get(msgid, msgid)
    if lang == "ko":
        return KO_TRANSLATIONS.get(msgid, msgid)
    if lang == "th":
        return TH_TRANSLATIONS.get(msgid, msgid)
    return msgid


def read_po_entries(po_path: Path) -> list[tuple[str, str]]:
    """Read .po file and return (msgid, msgstr) pairs."""
    with open(po_path, encoding="utf-8") as f:
        content = f.read()

    entries = []
    lines = content.split("\n")
    i = 0

    while i < len(lines):
        line = lines[i]
        if line.startswith("msgid "):
            msgid = line[6:].strip('"')
            i += 1
            while i < len(lines) and lines[i].strip().startswith('"'):
                msgid += lines[i].strip().strip('"')
                i += 1

            if i < len(lines) and lines[i].startswith("msgstr "):
                msgstr = lines[i][7:].strip('"')
                i += 1
                while i < len(lines) and lines[i].strip().startswith('"'):
                    msgstr += lines[i].strip().strip('"')
                    i += 1
                entries.append((msgid, msgstr))
        else:
            i += 1

    return entries


def create_po_file(
    lang: str, lang_name: str, entries: list[tuple[str, str]], output_path: Path
) -> None:
    """Create complete .po file with translations."""
    now = datetime.now().strftime("%Y-%m-%d %H:%M%z")

    header = f"""msgid ""
msgstr ""
"Project-Id-Version: ccBitTorrent 0.1.0\\n"
"Report-Msgid-Bugs-To: \\n"
"POT-Creation-Date: {now}\\n"
"PO-Revision-Date: {now}\\n"
"Last-Translator: ccBitTorrent Team\\n"
"Language-Team: {lang_name} Team\\n"
"Language: {lang}\\n"
"MIME-Version: 1.0\\n"
"Content-Type: text/plain; charset=UTF-8\\n"
"Content-Transfer-Encoding: 8bit\\n"

"""

    output_lines = [header]

    for msgid, english_msgstr in entries:
        if not msgid:  # Skip header
            continue

        # Get translation
        translation = get_translation(msgid, lang)
        if not translation or translation == msgid:
            translation = english_msgstr

        # Escape for .po format
        def escape(s: str) -> str:
            return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")

        output_lines.append(f'msgid "{escape(msgid)}"')
        output_lines.append(f'msgstr "{escape(translation)}"')
        output_lines.append("")

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(output_lines))


